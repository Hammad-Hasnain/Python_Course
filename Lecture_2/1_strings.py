# Four ways to define string
str1 = 'She said, "Hello!"' 
str2 =  "It's a great day." 
str3 = """She said, "It's a beautiful day!" and smiled."""       # Triple Double Quotes contains multiple lines of strings.
str4 = '''It's amazing how she said, "Don't worry about it."'''  # Triple Single Quotes also contains multiple lines of strings.

print(str1)  # Output: She said, "Hello!"
print(str2)  # Output: It's a great day.
print(str3)  # Output: She said, "It's a beautiful day!" and smiled.
print(str4)  # Output: It's amazing how she said, "Don't worry about it."