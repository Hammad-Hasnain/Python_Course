# A `while` loop repeatedly executes a block of code as long as a condition is True.
# Syntax: 
"""
while condition:
    # code to be executed for each item
""" 


# 1. Basic while loop
count = 1
while count <= 3:
    print(count)
    count += 1

# Output:
# 1
# 2
# 3



# 2. Reverse while loop 
n = 5
while n > 0:
    print(n)
    n -= 1